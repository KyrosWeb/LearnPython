# Python Variables and Data Types

# Integer variable
integer = 10
print(f"Integer: {integer}")

# Floating point variable
floating_point = 20.5
print(f"Floating point: {floating_point}")

# String variable
string = "Hello, World!"
print(f"String: {string}")

# Boolean variable
boolean = True
print(f"Boolean: {boolean}")

# List variable
list_var = [1, 2, 3, 4, 5]
print(f"List: {list_var}")

# Tuple variable
tuple_var = (1, 2, 3, 4, 5)
print(f"Tuple: {tuple_var}")

# Dictionary variable
dictionary = {"key": "value"}
print(f"Dictionary: {dictionary}")